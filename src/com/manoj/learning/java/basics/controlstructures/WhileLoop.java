package com.manoj.learning.java.basics.controlstructures;

public class WhileLoop {

	public static void main(String[] args) {
		//while
		int i = 0;
		while (i <= 10) {
		  System.out.println(i);
		  i++;
		}
		
		
		//Do while loop
		
		int j = 0;
		do {
		  System.out.println(i);
		  i++;
		}
		while (i < 5);
		

	}

}
