package com.manoj.learning.java.basics.booleans;

public class Main {

	public static void main(String[] args) {
		boolean isJavaFun = true;
		boolean isItGoingToBeEasier = false;
		
		System.out.println(isJavaFun);
		System.out.println(isItGoingToBeEasier);
		
		// Expressions that return boolean value
		System.out.println(10 > 9); //returns true
		System.out.println(9 > 10); //returns false
		
		
		int x = 10;
		System.out.println(x == 10); 
	}

}
